
public class Ejemplo {

	String atribEjemplo;
	int numero;

	public Ejemplo() {
		super();
		
	}

	public Ejemplo(String atribEjemplo) {
		super();
		this.atribEjemplo = atribEjemplo;
	}

	private String getAtribEjemplo() {
		return atribEjemplo;
	}

	private void setAtribEjemplo(String atribEjemplo) {
		this.atribEjemplo = atribEjemplo;
	}

	private int getNumero() {
		return numero;
	}

	private void setNumero(int numero) {
		this.numero = numero;
	}
	
	
}
